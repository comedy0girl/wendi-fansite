function latest_images_shortcode()
{
	$html = '<ul>';

	$args = array('child_of' => 341, 'sort_column' => 'post_date', 'sort_order' => 'desc');
 	$galleries = get_pages( $args );

	$count = 0;
	$limit = 6;
	foreach ($galleries as $gallery) {
		if (++$count > $limit) break;

		$args = array( 'post_type' => 'attachment', 'numberposts' => 1, 'post_status' => null, 'post_parent' => $gallery->ID); 
		$attachments = get_posts( $args );
		if ($attachments) {
			foreach ( $attachments as $image) {
				$html .= '<li><a href="' . get_permalink($gallery->ID) . '">' . wp_get_attachment_image($image->ID, 'thumbnail') . '</a></li>';
			}
		}
	}
	
	return $html . '</ul>';
	
}


